function findBookData(bookName) {
  // 精确匹配
  if (BOOK_DATA[bookName]) return BOOK_DATA[bookName];
  // 模糊匹配
  for (const key of Object.keys(BOOK_DATA)) {
    if (bookName.includes(key) || key.includes(bookName)) {
      return BOOK_DATA[key];
    }
  }
  return generateGenericBook(bookName);
}
